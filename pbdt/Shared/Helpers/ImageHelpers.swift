import Foundation
import UIKit

extension UIImage {
    
    class func nextArrow() -> UIImage {
        return UIImage(named: "nextArrow")!
    }
    
    class func backArrow() -> UIImage {
        return UIImage(named: "backArrow")!
    }
    
    class func forwardArrow() -> UIImage {
        return UIImage(named: "forwardArrow")!
    }
}
